# Faça um programa que leia um NÚMERO qualquer
# e mostre seu FATORIAL.

# Ex:
# 5! = 5 x 4 x 3 x 2 x 1 = 120

# z : uma variável inteira qualquer 

n = 0
count = 0
total = 0
n = int(input('Digite um número inteiro: '))
while n is not None:
    count =+ 1
    fatorial = (total + n) * (n - 1) * count
    print(fatorial)
